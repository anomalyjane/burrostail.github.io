			<h1>Interview: Spike E. Succulent</h1>
			<div class="timeStamp">April 15th, 2017</div>
				<p>We sat down with Spike to talk about art, philosophy, and the good life, from a succulent's point of view.</p>
				<p><b>What's up, man? How are you?</b><br>
					Good, good--things are good these days.</p>
				<p><b>So, tell us a little about your latest work.</b><br>
					Sure! Lately I've been sitting right by the window everyday--it's, well, it's meditative. I focus on just absorbing the energy coming to me--I think that's the real work of an artist. In my case, it's in the form of sunlight, you know, so it's all about the chlorophyll I create and how that substance interacts with the energy around me.</p>
				<p><b>Seems so relateable, even to me as a human.</b><br>
					Yeah, I mean, I believe that plantlife and humans have the potential for shared notions of art. I just don't believe that our living alongside each other could be merely utilitarian.</p>
				<p><b>As a succulent, do you ever feel misunderstood?</b><br>
					Oh, certainly. They're stacking young succulents in with the tropicals in big box stores all over the place. And without a drainage hole in the bottom of the pot. Get out of here with that nonsense! I'm a desert plant with desert plant needs. Don't throw me shade! [Laughs]. I need bright light for a good part of the day, as in my native habitat. Give me that and you've given me everything. I can do my life's work.</p>
