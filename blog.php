		<div class="timeStamp">May 8th, 2017</div>
		<p>It's May, and the spring-flowering trees are starting to shed their blooms. Bees are out and about. Sweaters have been consigned once again to the attic with that certain type of relief and gratitude that sun-starved New Englanders feel at the end of a long, gray, dismal winter. Two weeks ago we had been feeling so optimistic about the weather that we brought all of the succulents out into the light of day for the season.</p>
		<p>And yet... with temps tonight forecast to dip into the 30s (the 30s!), this afternoon we found ourselves dragging every last plant back into the garage, where hopefully they will stay safe from the cold. Although it's challenging to find any hard data about what temperatures various succulents can tolerate, a general rule of thumb I try to follow is not to leave them outside when the temps are going to dip below 45 degrees F. The only exceptions I make are creeping stonecrops--such as Sedum rupestre, which can be planted directly in the ground in fall, where they easily weather a Boston winter with their leaves intact--and a couple of types of crassula (such as hylotelephium), which die back in the winter but return as perennials early in the spring.</p>          
